#!/usr/bin/env node
/**
 * Writes local Supabase credentials to .env.development.local.
 * Run after `supabase start`. Never touches .env.local.
 */

import { execSync } from 'child_process'
import { writeFileSync } from 'fs'
import { join, dirname } from 'path'
import { fileURLToPath } from 'url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const OUT = join(ROOT, '.env.development.local')

function getSupabaseStatus() {
  try {
    const raw = execSync('npx supabase status -o json', {
      cwd: ROOT,
      encoding: 'utf8',
      stdio: ['pipe', 'pipe', 'pipe'],
    })
    return JSON.parse(raw)
  } catch (err) {
    console.error('Error running `supabase status`:', err.stderr?.toString() ?? err.message)
    console.error('Make sure `supabase start` has completed successfully.')
    process.exit(1)
  }
}

const s = getSupabaseStatus()

const get = (...keys) => {
  for (const k of keys) {
    const v = s[k] ?? s[k.toUpperCase()] ?? s[k.toLowerCase()]
    if (v) return v
  }
  return ''
}

const vars = {
  NEXT_PUBLIC_SUPABASE_URL:           get('API_URL', 'api_url', 'SUPABASE_URL'),
  NEXT_PUBLIC_SUPABASE_PUBLISHABLE_KEY: get('ANON_KEY', 'anon_key'),
  SUPABASE_SECRET_KEY:                get('SERVICE_ROLE_KEY', 'service_role_key'),
  DATABASE_URL_DIRECT:                `"${get('DB_URL', 'db_url', 'DATABASE_URL')}"`,
}

const lines = [
  '# Auto-generated by `npm run local:init` – do not edit manually',
  ...Object.entries(vars).map(([k, v]) => `${k}=${v}`),
]

writeFileSync(OUT, lines.join('\n') + '\n', 'utf8')

console.log(`✓ .env.development.local written`)
console.log(`  URL: ${vars.NEXT_PUBLIC_SUPABASE_URL}`)
